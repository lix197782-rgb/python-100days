int_var = 42          # 整数类型
float_var = 3.14         # 浮点数类型
complex_var = 1 + 2j    # 复数类型 

print(f"整数变量 int_var 的值: {int_var}, 类型: {type(int_var)}")          # <class 'int'>
print(f"浮点数变量 float_var 的值: {float_var}, 类型: {type(float_var)}")      # <class 'float'>
print(f"复数变量 complex_var 的值: {complex_var}, 类型: {type(complex_var)}")  # <class 'complex'>  
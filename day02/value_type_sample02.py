float_var1 = 10.5       # 浮点数类型
float_var2 = -3.14      # 负浮点数类型
float_var3 = 2.0e3      # 科学计数法表示的浮点数类型    
print(f"浮点数变量 float_var1 的值: {float_var1}, 类型: {type(float_var1)}")    # <class 'float'>
print(f"浮点数变量 float_var2 的值: {float_var2}, 类型: {type(float_var2)}")    # <class 'float'>
print(f"浮点数变量 float_var3 的值: {float_var3}, 类型: {type(float_var3)}")    # <class 'float'>   
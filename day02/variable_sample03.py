a = 100
b = 123.45
c = '123'
d = '100'
e = '123.45'
f = 'hello, world'
g = True
print(f"变量a的值: {a}, 类型: {type(a)}")  # <class 'int'>
print(f"变量b的值: {b}, 类型: {type(b)}")  # <class 'float'>
print(f"变量c的值: {c}, 类型:   {type(c)}")  # <class 'str'>
print(f"变量d的值: {d}, 类型:   {type(d)}")  # <class 'str'>
print(f"变量e的值: {e}, 类型:   {type(e)}")  # <class 'str'>
print(f"变量f的值: {f}, 类型:   {type(f)}")  # <class 'str'>
print(f"变量g的值: {g}, 类型:   {type(g)}")  # <class 'bool'>